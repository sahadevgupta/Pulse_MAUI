using System;

namespace Pulse_MAUI.Helpers
{
	/// <summary>
	/// Class with all PCA specific fontsizes to use in the application. 
	/// Fogbugz Case:
	/// Author: Manuel Dambrine
	/// Created: 29/03/2013
	/// </summary>
	public class FontSizes
	{
		/// <summary>
		/// The micro font size.
		/// </summary>
		public static readonly double Micro = 12 * App.SizeFormula;

		/// <summary>
		/// The small font size.
		/// </summary>
		public static readonly double Small = 14 * App.SizeFormula;

		/// <summary>
		/// The medium font size.
		/// </summary>
		public static readonly double Medium = 16 * App.SizeFormula;

		/// <summary>
		/// The default font size.
		/// </summary>
		public static readonly double Default = 18 * App.SizeFormula;

		/// <summary>
		/// The large font size.
		/// </summary>
		public static readonly double Large = 21 * App.SizeFormula;


    }
}
